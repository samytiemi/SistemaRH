    private void btnAttSistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttSistActionPerformed
        if(linhaClicada != -1){
            Sistema sistema = tabelaSist.getSistema(linhaClicada);

            sistema.setSistema(campoSist.getText());

            SistemaDAO dao = null;
            try {
                dao = new SistemaDAO();
                dao.updateSistema(sistema);
                JOptionPane.showMessageDialog(null,"Sistema atualizado com sucesso!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Erro ao atualizar no banco de dados. E="+ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
            //Atualiza tabela
            tabelaSist.fireTableRowsUpdated(linhaClicada, linhaClicada);

        }
    }//GEN-LAST:event_btnAttSistActionPerformed

    private void btnInserirSistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirSistActionPerformed
        Sistema sistema = new Sistema();
        sistema.setSistema(campoSist.getText());
        campoSist.setText(null);
        SistemaDAO dao = null;
        try {
            dao = new SistemaDAO();
            dao.insertSistema(sistema);
            JOptionPane.showMessageDialog(null,"Sistema inserido com sucesso!");
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao atualizar no banco de dados. E="+ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        tabelaSist.insetSistema(sistema);
    }//GEN-LAST:event_btnInserirSistActionPerformed

