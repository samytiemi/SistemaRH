    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        //Pega a linha clicada
        linhaClicada = tabela.rowAtPoint(evt.getPoint());
        //Pega o contato da linha clidada
        Sistema sistema = tabelaSist.getSistema(linhaClicada);
        //Seta os dados nos componentes
        campoSist.setText(sistema.getSistema());
    }//GEN-LAST:event_tabelaMouseClicked

    private void btnExcluirSistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirSistActionPerformed
        try {
            SistemaDAO dao = new SistemaDAO();
            int[] linhasSelecionadas = tabela.getSelectedRows();
            List<Sistema> listaExcluir = new ArrayList();
            for (int i = 0; i < linhasSelecionadas.length; i++) {
                Sistema sistema = tabelaSist.getSistema(linhasSelecionadas[i]);
                dao.deleteSistema(sistema);
                listaExcluir.add(sistema);

            }
            for(Sistema sistema:listaExcluir){
                tabelaSist.deleteSistema(sistema);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Erro ao realizar exclusÃ£o de sistemas.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirSistActionPerformed

