    private void btnInserirDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirDepActionPerformed
        Departamento dep = new Departamento();
        dep.setDepartamento(campoDep.getText());
        campoDep.setText(null);
        DepartamentoDAO dao = null;
        try {
            dao = new DepartamentoDAO();
            dao.insertDepartamento(dep);
            JOptionPane.showMessageDialog(null,"Departamento inserido com sucesso!");
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao atualizar no banco de dados. E="+ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        tabelaDep.insetDepartamento(dep);
    }//GEN-LAST:event_btnInserirDepActionPerformed

    private void btnAttDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttDepActionPerformed
        if(linhaClicada != -1){
            Departamento departamento = tabelaDep.getDepartamento(linhaClicada);
            
            departamento.setDepartamento(campoDep.getText());
            
            DepartamentoDAO dao = null;
            try {
                dao = new DepartamentoDAO();
                dao.updateDepartamento(departamento);
                JOptionPane.showMessageDialog(null,"Departamento atualizado com sucesso!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Erro ao atualizar no banco de dados. E="+ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
            //Atualiza tabela
            tabelaDep.fireTableRowsUpdated(linhaClicada, linhaClicada);
            
        }
    }//GEN-LAST:event_btnAttDepActionPerformed
    
