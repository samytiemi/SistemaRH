    private void btnExcluirDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirDepActionPerformed
        try {
            DepartamentoDAO dao = new DepartamentoDAO();
            int[] linhasSelecionadas = tabela.getSelectedRows();
            List<Departamento> listaExcluir = new ArrayList();
            for (int i = 0; i < linhasSelecionadas.length; i++) {
                Departamento departamento = tabelaDep.getDepartamento(linhasSelecionadas[i]);
                dao.deleteDepartamento(departamento);
                listaExcluir.add(departamento);

            }
            for(Departamento departamento:listaExcluir){
                tabelaDep.deleteDepartamento(departamento);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Erro ao realizar exclusÃ£o de contatos.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirDepActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        //Pega a linha clicada
        linhaClicada = tabela.rowAtPoint(evt.getPoint());
        //Pega o contato da linha clidada
        Departamento departamento = tabelaDep.getDepartamento(linhaClicada);
        //Seta os dados nos componentes
        campoDep.setText(departamento.getDepartamento());
    }//GEN-LAST:event_tabelaMouseClicked

